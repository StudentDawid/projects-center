/**
 * Item definitions for Ateria Idle
 * Data-Driven Design - all item data in one place
 */

import type { ItemRarity, EquipmentSlot, EquipmentStats } from '~/entities/ateria-idle/warrior';

// ============================================
// ITEM TYPES
// ============================================

export type ItemCategory =
  | 'material'      // Crafting materials
  | 'consumable'    // Food, potions
  | 'equipment'     // Weapons, armor
  | 'quest'         // Quest items
  | 'currency'      // Special currencies
  | 'collectible';  // Achievements, rare drops

export interface Item {
  id: string;
  name: string;
  description: string;
  category: ItemCategory;
  icon: string;
  rarity: ItemRarity;
  stackable: boolean;
  maxStack: number;
  sellPrice: number;
  buyPrice?: number; // If purchasable
}

export interface MaterialItem extends Item {
  category: 'material';
  tier: number;
  usedIn?: string[]; // Recipe IDs
}

export interface ConsumableItem extends Item {
  category: 'consumable';
  effect: ConsumableEffect;
  cooldown: number; // ticks
}

export interface ConsumableEffect {
  type: 'heal' | 'buff' | 'restore_resource';
  value: number;
  isPercent?: boolean;
  duration?: number; // For buffs
  stat?: keyof EquipmentStats;
}

export interface EquipmentItem extends Item {
  category: 'equipment';
  slot: EquipmentSlot;
  stats: Partial<EquipmentStats>;
  requirements: {
    level?: number;
    slayerLevel?: number;
  };
}

