// ============================================
// HELPER FUNCTIONS
// ============================================

export function getItem(id: string): Item | undefined {
  return MATERIALS[id] || CONSUMABLES[id];
}

export function getMaterial(id: string): MaterialItem | undefined {
  return MATERIALS[id];
}

export function getConsumable(id: string): ConsumableItem | undefined {
  return CONSUMABLES[id];
}

export function getItemsByTier(tier: number): MaterialItem[] {
  return Object.values(MATERIALS).filter(m => m.tier === tier);
}

export function getItemsByRarity(rarity: ItemRarity): Item[] {
  return [
    ...Object.values(MATERIALS).filter(m => m.rarity === rarity),
    ...Object.values(CONSUMABLES).filter(c => c.rarity === rarity),
  ];
}
